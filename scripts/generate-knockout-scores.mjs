/**
 * Regenerate src/data/knockout-stage-scores.js from pool entries + bundled live results.
 * Run after scripts/fetch-live-results.mjs (also runs in the GitHub Pages deploy workflow).
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { POOL_ENTRIES } from '../src/data/pool-entries.js';
import { scoreKnockoutPredictions, createEmptyKnockoutPredictions } from '../src/lib/scoring.js';
import { getLockedKnockoutResults } from '../src/lib/knockout-bracket.js';
import { KNOCKOUT_MATCHES } from '../src/data/knockout.js';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const LIVE_PATH = join(ROOT, 'public', 'data', 'live-results.json');
const OUT_PATH = join(ROOT, 'src', 'data', 'knockout-stage-scores.js');

function loadLiveResults() {
  try {
    return JSON.parse(readFileSync(LIVE_PATH, 'utf8'));
  } catch {
    console.warn(`No ${LIVE_PATH} — using locked knockout results only.`);
    return null;
  }
}

function formatMap(obj, indent = '  ') {
  return Object.entries(obj)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([key, value]) => `${indent}'${key}': ${value},`)
    .join('\n');
}

function mergeKnockoutForScoring(live) {
  const merged = createEmptyKnockoutPredictions();
  const locked = getLockedKnockoutResults();
  const liveKnockout = live?.knockout ?? {};
  for (const match of KNOCKOUT_MATCHES) {
    merged[match.id] = liveKnockout[match.id] || locked[match.id] || '';
  }
  return merged;
}

function main() {
  const live = loadLiveResults();
  const knockoutResults = mergeKnockoutForScoring(live);
  const generatedAt = live?.updatedAt ?? new Date().toISOString();

  const byEmail = {};
  const byName = {};

  for (const entry of POOL_ENTRIES) {
    const scored = scoreKnockoutPredictions(entry.knockout, knockoutResults);
    const email = (entry.email ?? '').trim().toLowerCase();
    const name = (entry.name ?? '').trim().toLowerCase();
    if (email) byEmail[email] = scored.points;
    if (name) byName[name] = scored.points;
  }

  const contents = `/**
 * Knockout points per player (max 80).
 * Auto-generated by scripts/generate-knockout-scores.mjs — do not edit by hand.
 * Source snapshot: public/data/live-results.json (${generatedAt})
 */
export const KNOCKOUT_STAGE_SCORES_BY_EMAIL = {
${formatMap(byEmail)}
};

/** Name fallback when email is missing or differs from the Excel row. */
export const KNOCKOUT_STAGE_SCORES_BY_NAME = {
${formatMap(byName)}
};

export function getFinalizedKnockoutPoints(entry) {
  const email = (entry.email ?? '').trim().toLowerCase();
  if (email && KNOCKOUT_STAGE_SCORES_BY_EMAIL[email] != null) {
    return KNOCKOUT_STAGE_SCORES_BY_EMAIL[email];
  }
  const name = (entry.name ?? '').trim().toLowerCase();
  if (name && KNOCKOUT_STAGE_SCORES_BY_NAME[name] != null) {
    return KNOCKOUT_STAGE_SCORES_BY_NAME[name];
  }
  return null;
}
`;

  writeFileSync(OUT_PATH, contents, 'utf8');
  console.log(
    `Wrote ${OUT_PATH} (${Object.keys(byEmail).length} players, KO ${Math.min(...Object.values(byEmail))}–${Math.max(...Object.values(byEmail))} pts)`
  );

  const bundledPath = join(ROOT, 'src', 'data', 'bundled-live-results.js');
  const bundledContents = `/**
 * Bundled live results snapshot for offline / first-paint scoring.
 * Auto-generated by scripts/generate-knockout-scores.mjs — do not edit by hand.
 */
export const BUNDLED_LIVE_RESULTS = ${JSON.stringify(live ?? { knockout: knockoutResults, updatedAt: generatedAt, source: 'locked-only', groupsWithMatches: 12, groups: {}, meta: {}, knockoutMatchesFinished: Object.values(knockoutResults).filter(Boolean).length }, null, 2)};
`;
  writeFileSync(bundledPath, bundledContents, 'utf8');
  console.log(`Wrote ${bundledPath}`);
}

main();
